#include "../includes/fillit.h"

size_t		ft_sqrt(size_t nb)
{
	size_t		c;
	size_t		a;

	c = 2;
	a = c * c;
	while (a < nb)
	{
		c++;
		a = c * c;
	}
	return (c);
}
